let x = 5.25;
let y = 10;
let z = 15;

console.log(Math.PI); // Outputs the value of PI (3.141592653589793)
console.log(Math.round(x)); // Rounds x to the nearest integer (5)
console.log(Math.floor(x)); // Rounds x down to the nearest integer (5)
console.log(Math.ceil(x)); // Rounds x up to the nearest integer (6)
console.log(Math.trunc(x)); // Removes the fractional part of x (5)
console.log(Math.pow(x, y)); // Raises x to the power of y (5.25^10)
console.log(Math.sqrt(x)); // Calculates the square root of x
console.log(Math.log(x)); // Calculates the natural logarithm (base e) of x
console.log(Math.sign(x)); // Returns the sign of x (1 for positive, -1 for negative, 0 for zero)
console.log(Math.cos(x)); // Calculates the cosine of x (x is in radians)
console.log(Math.tan(x)); // Calculates the tangent of x (x is in radians)
console.log(Math.abs(x)); // Returns the absolute value of x
console.log(Math.max(x, y, z)); // Returns the largest value among x, y, and z
console.log(Math.min(x, y, z)); // Returns the smallest value among x, y, and z
